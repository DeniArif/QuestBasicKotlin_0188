package com.example.mytugas

fun main() {
    // Byte
    val ByteValue: Byte = 10
    println("Byte: $ByteValue")


    // Int
    val intValue: Int = 30
    println(intValue)

    // Float
    val floatValue: Float = 50.6f
    println(floatValue)


    // Double
    val doubleValue: Double = 60.6
    println(doubleValue)

}
